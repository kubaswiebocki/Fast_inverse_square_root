import struct
#####################################################################

# Otwarcie pliku
#####################################################################
e = 0
def float_to_bin(num):
    bits, = struct.unpack('!I', struct.pack('!f', num))
    return "{:032b}".format(bits)

f = open("InputData.mem", 'w')
f.write('')
f.close()
f = open("InputData.mem", 'a')
#####################################################################

